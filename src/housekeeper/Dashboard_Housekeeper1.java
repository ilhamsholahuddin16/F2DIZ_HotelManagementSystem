    private void tampilkanDataKamar() {
        try {
            Connection conn = DatabaseConnection.connect();
            String sql = "SELECT nomor_kamar, tipe, harga_kamar, status FROM kamar";

            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No Kamar");
            model.addColumn("Tipe");
            model.addColumn("Harga Kamar");
            model.addColumn("Status");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nomor_kamar"),
                    rs.getString("tipe"),
                    rs.getString("harga_kamar"),
                    rs.getString("status")
                });
            }

            jTable1.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void tampilkanLoginHousekeeper() {
        try {
            Connection conn = DatabaseConnection.connect();

            String sql = "SELECT l.id_pegawai, l.username, l.nama_pegawai, l.akses "
                    + "FROM usersistem l "
                    + "JOIN log_login u ON l.username = u.username "
                    + "WHERE u.akses = 'Housekeeper' AND u.status = 'sukses' "
                    + "ORDER BY u.login_time DESC LIMIT 1";

            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                jLabel7.setText(rs.getString("id_pegawai"));
                jLabel10.setText(rs.getString("username"));
                jLabel12.setText(rs.getString("nama_pegawai"));
                jLabel14.setText(rs.getString("akses"));
            } else {
                JOptionPane.showMessageDialog(this, "Belum ada login Housekeeper.");
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }

