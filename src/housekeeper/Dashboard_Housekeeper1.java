    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nomorKamar = jTextField1.getText();
        String tipeKamar = jComboBox1.getSelectedItem().toString();
        String statusKamar = jComboBox2.getSelectedItem().toString();

        // Validasi sederhana
        if (nomorKamar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nomor kamar tidak boleh kosong.");
            return;
        }

        try {
            Connection conn = config.DatabaseConnection.connect(); // Pastikan sudah ada class koneksi
            String sql = "UPDATE kamar SET tipe=?, status=? WHERE nomor_kamar=?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, tipeKamar);
            pst.setString(2, statusKamar);
            pst.setString(3, nomorKamar);
            int updated = pst.executeUpdate();

            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "Perubahan berhasil disimpan.");
                tampilkanDataKamar();
            } else {
                JOptionPane.showMessageDialog(this, "Nomor kamar tidak ditemukan.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField2.getText().trim();

        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan kata kunci pencarian terlebih dahulu.");
            return;
        }

        try {
            Connection conn = config.DatabaseConnection.connect();
            String sql = "SELECT * FROM kamar WHERE nomor_kamar LIKE ? OR tipe LIKE ? OR harga_kamar LIKE ? OR status LIKE ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, "%" + keyword + "%");
            pst.setString(2, "%" + keyword + "%");
            pst.setString(3, "%" + keyword + "%");
            pst.setString(4, "%" + keyword + "%");

            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nomor Kamar");
            model.addColumn("Tipe");
            model.addColumn("Harga");
            model.addColumn("Status");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nomor_kamar"),
                    rs.getString("tipe"),
                    rs.getString("harga_kamar"),
                    rs.getString("status")
                });
            }

            jTable1.setModel(model);

            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

